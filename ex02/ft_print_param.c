int main(int argc, char const *argv[])
{
    int i;
    int j;

    i = 0;
    j = 1;
    while (j < argc)
    {
        while (argv[j][i] != '\0')
        {
            ft_putchar(argv[j][i]);
            ++i;
        }
        ft_putchar('\n');
        ++j;
    }
    return (0);
}
